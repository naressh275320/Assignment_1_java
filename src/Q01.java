//1.	Write a Java program to print the sum of two numbers, subtraction, multiplication, division. (normal way)

public class Q01 {
    public static void main(String args[]){
        
        //Initialization and Declaration of variables
        int a = 6, b = 2;  

        //Displaying the output for addition of two declared variables
        System.out.println("Addition of two number:        "+ (a+b));  

        //Displaying the output for subraction of two declared variables
        System.out.println("Subraction of two number:      "+ (a-b)); 
        
        //Displaying the output for division of two declared variables
        System.out.println("Division of two number:        "+ (a/b));  

        //Displaying the output for multiplication of two declared variables
        System.out.println("Multiplication of two number:  "+ (a*b));  
        
    }
}
